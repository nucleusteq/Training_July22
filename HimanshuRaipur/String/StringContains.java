public class StringContains{
	public static void main(String[] args){
		String str="Welcome to NucleusTeq!";
		System.out.println(str.contains("Welcome"));// it will print true as the string contains basic in the string.
		System.out.println(str.contains("himanshu")); // it will print false as the string does not contains himanshu.
		System.out.println(str.contains("to"));// it will print true as the string contains va in it.

	}
}