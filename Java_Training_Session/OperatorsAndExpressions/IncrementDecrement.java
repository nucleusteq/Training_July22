public class IncrementDecrement{
	public static void main(String[] args){
		int num=10;
		System.out.println(++num); // it will print 11 and the value of num will be 11
		System.out.println(num++); // it will print 11 and the value of num will be 12
		System.out.println(++num); // it will print 13 and the value of num will be 13
		System.out.println(num--); // it will print 13 and the value of num will be 12
		System.out.println(--num); // it will print 11 and the value of num will be 11
		System.out.println(num++); // it will print 11 and the value of num will be 12
		System.out.println(num);   // it will print 12
	}
}