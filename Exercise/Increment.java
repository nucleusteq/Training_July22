class Increment
{
   public static void main(String args[]) 
    {   
  int inc1 = 1;
  int inc2 = 2;
  int inc3 = 3;
 System.out.println(inc1);
 System.out.println(inc2);
 System.out.println(inc3);
 ++inc1;  
 ++inc2;  
 ++inc3; 
 System.out.println(inc1);
 System.out.println(inc2);
 System.out.println(inc3);
 --inc1;
 --inc2;
 --inc3;
 System.out.println(inc1);
 System.out.println(inc2);
 System.out.println(inc3);
inc1++;  
 inc2++;  
 inc3++;
 System.out.println(inc1);
 System.out.println(inc2);
 System.out.println(inc3);
}
}